#!/usr/bin/env node
// Content integrity gate. Scans tracked source and documentation for content
// that must never ship in this repository:
//
//   1. Em dash characters in prose, source, tests, or docs.
//   2. Tool attribution, generated-by text, session links, or co-author
//      trailers.
//
// CLAUDE.md is the single allowed exception because it intentionally lists the
// forbidden strings as examples. Exits nonzero with a file and line report when
// a violation is found. Run locally through "npm run check" and in CI.

import { execSync } from "node:child_process";
import { readFileSync } from "node:fs";

// CLAUDE.md intentionally documents the forbidden strings as examples, and
// this script necessarily contains its own detection patterns.
const EXCLUDED_FILES = new Set([
  "CLAUDE.md",
  "scripts/check-content-integrity.mjs",
]);
const EXCLUDED_PREFIXES = ["public/", "package-lock.json"];

const TEXT_EXTENSIONS = new Set([
  ".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs", ".py", ".md", ".mdx",
  ".json", ".yml", ".yaml", ".css", ".html", ".txt", ".toml", ".ini",
  ".cfg", ".env", ".example",
]);

const RULES = [
  { name: "em dash", pattern: /—/ },
  { name: "attribution: Generated by", pattern: /Generated (by|with) Claude/i },
  { name: "attribution: Claude Code", pattern: /Claude Code/ },
  { name: "attribution: session link", pattern: /claude\.ai\/code/i },
  { name: "attribution: co-author trailer", pattern: /Co-Authored-By/i },
  { name: "attribution: AI-generated footer", pattern: /AI-generated/i },
];

function trackedFiles() {
  return execSync("git ls-files", { encoding: "utf8" })
    .split("\n")
    .filter(Boolean);
}

function isCheckable(file) {
  if (EXCLUDED_FILES.has(file)) return false;
  if (EXCLUDED_PREFIXES.some((p) => file.startsWith(p))) return false;
  const dot = file.lastIndexOf(".");
  if (dot === -1) return true;
  return TEXT_EXTENSIONS.has(file.slice(dot).toLowerCase());
}

const violations = [];

for (const file of trackedFiles()) {
  if (!isCheckable(file)) continue;
  let content;
  try {
    content = readFileSync(file, "utf8");
  } catch {
    continue;
  }
  const lines = content.split("\n");
  for (const rule of RULES) {
    lines.forEach((line, i) => {
      if (rule.pattern.test(line)) {
        violations.push(`${file}:${i + 1}: ${rule.name}`);
      }
    });
  }
}

if (violations.length > 0) {
  console.error("Content integrity violations found:");
  for (const v of violations) console.error(`  ${v}`);
  process.exit(1);
}

console.log("Content integrity check passed.");
